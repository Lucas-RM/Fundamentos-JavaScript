function operacoes(num1, num2) {
    console.log(`${num1} + ${num2} = ${num1+num2}`)
    console.log(`${num1} - ${num2} = ${num1-num2}`)
    console.log(`${num1} * ${num2} = ${num1*num2}`)
    console.log(`${num1} / ${num2} = ${num1/num2}`)
}

operacoes(7, 9)
console.log()
operacoes(9, 7)
console.log()
operacoes(1, 2)